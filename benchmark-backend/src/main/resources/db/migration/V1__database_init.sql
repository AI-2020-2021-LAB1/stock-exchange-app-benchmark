CREATE SEQUENCE CONFIGURATION_SEQUENCE
	START WITH 1
	INCREMENT BY 1
	MINVALUE 1
	NO CYCLE;

CREATE SEQUENCE TEST_SEQUENCE
	START WITH 1
	INCREMENT BY 1
	MINVALUE 1
	NO CYCLE;

CREATE SEQUENCE RESPONSE_SEQUENCE
	START WITH 1
	INCREMENT BY 50
	MINVALUE 1
	NO CYCLE;

CREATE TABLE CONFIGURATIONS
(
	ID bigint DEFAULT NEXTVAL('CONFIGURATION_SEQUENCE') PRIMARY KEY,
	NAME varchar(255) NOT NULL,
	CREATED_AT timestamp NOT NULL,
	ARCHIVED boolean NOT NULL,
	CERTAINTY_LEVEL decimal NOT NULL,
	LOGIN_ALL_STOCKS decimal NOT NULL,
	LOGIN_OWNED_STOCKS decimal NOT NULL,
	LOGIN_USER_ORDERS decimal NOT NULL,
	LOGIN_MAKE_ORDER decimal NOT NULL,
	ALL_STOCKS_MAKE_ORDER decimal NOT NULL,
	ALL_STOCKS_END decimal NOT NULL,
	OWNED_STOCKS_MAKE_ORDER decimal NOT NULL,
	OWNED_STOCKS_END decimal NOT NULL,
	USER_ORDERS_MAKE_ORDER decimal NOT NULL,
	USER_ORDERS_END decimal NOT NULL,
	USER_ORDER_DELETE_ORDER decimal NOT NULL,
	MAKE_ORDER_BUY_ORDER decimal NOT NULL,
	MAKE_ORDER_SELL_ORDER decimal NOT NULL,
	NO_OF_OPERATIONS decimal NOT NULL,
	NO_OF_MONEY decimal NOT NULL
);

CREATE TABLE TESTS
(
	ID bigint DEFAULT NEXTVAL('TEST_SEQUENCE') PRIMARY KEY,
	CONFIGURATION_ID bigint NOT NULL,
	START_DATE timestamp NOT NULL,
	END_DATE timestamp NOT NULL,
	FOREIGN KEY (CONFIGURATION_ID)
	REFERENCES CONFIGURATIONS (ID)
);

CREATE TABLE RESPONSES
(
	ID bigint DEFAULT NEXTVAL('RESPONSE_SEQUENCE') PRIMARY KEY,
	TEST_ID bigint NOT NULL,
	ENDPOINT varchar(255) NOT NULL,
	STATUS_CODE int NOT NULL,
	METHOD varchar(255) NOT NULL,
	RESPONSE_DATE timestamp NOT NULL,
	USERS_LOGGED_IN int NOT NULL,
	REQUEST_RESPONSE_TIME decimal NOT NULL,
	OPERATION_TIME decimal NOT NULL,
	FOREIGN KEY (TEST_ID)
	REFERENCES TESTS (ID)
);
